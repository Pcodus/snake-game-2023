#include <vector>
#include <iostream>
using namespace std;

class Gatepair {
public:
    vector<pair<int, int>> gate1;
    vector<pair<int, int>> gate2;
    int dur;
    
    Gatepair(int x1, int y1, int x2, int y2) {
        gate1.push_back(make_pair(x1, y1));
        gate2.push_back(make_pair(x2, y2));
        dur = 0;
    }
};